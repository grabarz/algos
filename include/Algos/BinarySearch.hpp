template <typename I, typename V>
I BinarySearch(I begin, I end, V value)
{
	auto size = end - begin;

	while (size > 0)
	{
		
		auto it = begin + size / 2;

		if (value == *it)
			return it;
		else if (value > *it)
			begin = it + 1;
		else
			end = it;

		size = end - begin;
	}

	return end;
}
